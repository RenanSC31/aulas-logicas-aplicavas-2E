public class Main {
    public static void main(String[] args) {
        //laço contador-while
        int cont=0;
        while(cont<=100){
            System.out.println("contador="+ cont);
            //incremento
            cont = cont+1;
        }
        //laço do para faça - for
        for(int x=0;x<100;x++){
            System.out.println("contador="+ x);
            //incremento
        }
//laço fazer equanto-do while
        int conta=1;
        do{
            cont++;
            //processo
        }while(conta<=33);
    }
}